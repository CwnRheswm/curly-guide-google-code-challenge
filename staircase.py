def solution(n):
  steps = [0 for _ in range(n+1)]
  steps[0] = 1
  steps[1] = 1

  for i in range(2, n+1):
    for k in range(n, i-1, -1):
      steps[k] = steps[k]+steps[k-i]
  steps = [item - 1 for item in steps]

  return steps[n]
print(solution(20))
